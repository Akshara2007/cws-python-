"""
Ask the user to enter a number between (1-7)
"""
# WeekDay=WD
WD=int(input("input Enter WD number(1-7)= "))
if WD==1:
    print("monday")
elif WD==2:
    print("tuesday")
elif WD==3:
    print("wednesday")
elif WD==4:
    print("thursday")
elif WD==5:
    print("friday")
elif WD==6:
    print("saturday")
else:
    print("sunday")
